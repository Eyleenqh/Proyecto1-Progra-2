package domain;

import java.io.Serializable;

/**
 *
 * @author Steven
 */
public class Material implements Serializable{

    private int year;

    public Material() {
        this.year = 0;
    }

    public Material(int year) {
        this.year = year;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    @Override
    public String toString() {
        return ", Year=" + year;
    }
    
    
}
