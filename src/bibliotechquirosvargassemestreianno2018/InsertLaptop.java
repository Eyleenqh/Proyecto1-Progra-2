    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        boolean camera;
        int code;
        //Este trycontrola la creaci칩n del archivo
        try {
            if (file.exists()) {
            } else {
                file.createNewFile();
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error en el procedimiento. Vuelva a intentarlo.");
        }

        //Este if le da un valor a cada radio Button
        if (yesBtn.isSelected()) {
            camera = true;
        } else {
            camera = false;
        }
//Estos if comprueba que la informacion este completa y correcta 
        if (this.brandText.getText().isEmpty() || this.colorText.getText().isEmpty()|| this.sizeText.getText().isEmpty() || this.yearText.getText().isEmpty() || this.quantityText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese toda la informaci칩n solicitada.");
        } else {
            if (Integer.parseInt(this.yearText.getText()) < 1000) {
                JOptionPane.showMessageDialog(this, "Por favor, ingrese informaci칩n v치lida");
            } else {
                try {
                    if (this.laptopFile.verifyBrand(this.brandText.getText(), Integer.parseInt(this.quantityText.getText()))) {
                        this.brandText.setText("");
                        this.colorText.setText("");
                        this.yearText.setText("");
                        this.sizeText.setText("");
                        this.quantityText.setText("");
                    } else {
                        code=Integer.parseInt(laptopFile.createCode());
                        laptop = new Laptop(Integer.parseInt(this.sizeText.getText()),this.colorText.getText(), camera, Integer.parseInt(this.quantityText.getText()),code,this.brandText.getText(), Integer.parseInt(this.yearText.getText()));
                        this.brandText.setText("");
                        this.colorText.setText("");
                        this.yearText.setText("");
                        this.sizeText.setText("");
                        this.quantityText.setText("");

                        //Este try ingresa la informacion en la posici칩n final del archivo
                        try {
                            this.laptopFile.addEndRecord(laptop);
                        } catch (IOException ex) {
                            Logger.getLogger(InsertLaptop.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error al guardar la informaci칩n. Verifique que la informaci칩n est칠 correcta.");
                }
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed


